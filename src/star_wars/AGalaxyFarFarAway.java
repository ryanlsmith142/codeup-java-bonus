package star_wars;

abstract class AGalaxyFarFarAway {
    private String name;
    private String race;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setRace(String race) {
        this.race = race;
    }

    public String getRace() {
        return this.race;
    }

}
